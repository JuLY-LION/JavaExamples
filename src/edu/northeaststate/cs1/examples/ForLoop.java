package edu.northeaststate.cs1.examples;

public class ForLoop {
    public static void main(String[] args) {
        int[] list = {1,2,3,4,5,6,7,};

        for (int i = 0; i < list.length; i++) {
            System.out.print(list[i] + " ");
        }
        System.out.println();
        for (int i = 0; i < list.length; i++) {
            System.out.print(list[i] + " ");
        }

    }
}
